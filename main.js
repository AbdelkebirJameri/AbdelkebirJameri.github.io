/* ============================================================
   TYPEWRITER EFFECT
   ============================================================ */
let typedInterval = null;
let typedTimeout  = null;

function restartTyped() {
  clearInterval(typedInterval);
  clearTimeout(typedTimeout);
  const el = document.getElementById('typedText');
  if (el) el.textContent = '';
  startTyped();
}

function startTyped() {
  const el = document.getElementById('typedText');
  if (!el) return;
  const strings = typedStrings[currentLang];
  let si = 0, ci = 0, deleting = false;

  function tick() {
    const str = strings[si];
    if (!deleting) {
      el.textContent = str.slice(0, ci + 1);
      ci++;
      if (ci === str.length) {
        deleting = true;
        typedTimeout = setTimeout(tick, 2200);
        return;
      }
    } else {
      el.textContent = str.slice(0, ci - 1);
      ci--;
      if (ci === 0) {
        deleting = false;
        si = (si + 1) % strings.length;
      }
    }
    typedTimeout = setTimeout(tick, deleting ? 40 : 65);
  }
  tick();
}

/* ============================================================
   SCROLL REVEAL
   ============================================================ */
function initReveal() {
  const items = document.querySelectorAll('.reveal');
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const delay = parseInt(entry.target.getAttribute('data-delay') || '0');
        setTimeout(() => {
          entry.target.classList.add('visible');
        }, delay);
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.08, rootMargin: '0px 0px -40px 0px' });

  items.forEach(item => observer.observe(item));
}

/* ============================================================
   NAVBAR SCROLL STATE + ACTIVE LINK
   ============================================================ */
function initNavbar() {
  const navbar = document.getElementById('navbar');
  const links  = document.querySelectorAll('.nav-link');
  const sections = Array.from(document.querySelectorAll('section[id]'));

  function onScroll() {
    navbar.classList.toggle('scrolled', window.scrollY > 20);

    let current = '';
    sections.forEach(sec => {
      if (window.scrollY >= sec.offsetTop - 120) current = sec.id;
    });

    links.forEach(link => {
      link.classList.toggle('active', link.getAttribute('href') === '#' + current);
    });
  }

  window.addEventListener('scroll', onScroll, { passive: true });
  onScroll();
}

